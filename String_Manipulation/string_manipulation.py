## String Manipulation
# Concatination

First_name = "Chandan"
Last_name = "Gowda"
Full_name = First_name + " " + Last_name
print(Full_name)


# Repetition
  
Message =  "Warning!"
print(Message*10)

## String Methods
 # Upper case()
Message = "Warning!"
print(Message.upper())

# Lower case()
Message = "Warning!"
print(Message.lower())


# Strip ()
Message = "Warning!"
print(Message.strip()*2)


# Replace ()
Message = "Warning!"
print(Message.replace("Warning","Error"))


## Accessing string character

Text = "Python"
print(Text[2])
print(Text[0])

# you can accessing negative indexing

print(Text[-2])
print(Text[-3])

## Slicing string

print(Text[2:5])
print(Text[2:])
print(Text[0:])

